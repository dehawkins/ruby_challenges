fav_color = 'orange'
if (fav_color == 'blue')
  puts "Navy blue like the ocean !"
elsif (fav_color == 'green')
  puts "Green like my grass in the summer."
elsif (fav_color == 'yellow')
  puts "Yellow sunflowers are so gorgeous and huge!"
elsif (fav_color == 'orange')
  puts "Orange is the color of a great citrus fruit. Oranges."
elsif (fav_color == 'Aqua')
  puts "Aqua like the color of the ocean in the Caribbean. I want to go there now."
elsif (fav_color == 'purple')
  puts "Purple plums are the tastiest."
else
  puts "Hmm, well I don't know what your favorite color is!"
end
